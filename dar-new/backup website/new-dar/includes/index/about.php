                    Mandate and Functions
                    To lead in the implementation of the Comprehensive Agrarian Reform Program (CARP) through Land
                    Tenure Improvement (LTI), Agrarian Justice and Coordinated delivery of essential Support Services to
                    client beneficiaries.
                </p>
                <br />
                <p>
                    To provide Land Tenure security to landless farmers through land acquisition and distribution;
                    leasehold arrangements implementation and other LTI services;
                    To provide legal intervention to Agrarian Reform Beneficiaries (ARBS) through adjudication of
                    agrarian cases and agrarian legal assistance;
                    To implement, facilitate and coordinate the delivery of support services to ARBs through Social
                    Infrastructure and Local Capability Building (SILCAB); Sustainable Agribusiness and Rural Enterprise
                    Development (SARED); and Access Facilitation and Enhancement Services (AFAES).
                </p>
                <br />
                <p>
                    Mission Statement
                    DAR is the lead government agency that holds and implements comprehensive and genuine agrarian
                    reform which actualizes equitable land distribution, ownership, agricultural productivity, and
                    tenurial security for, of and with the tillers of the land towards the improvement of their quality
                    of life.
                </p>
                <br />
                <p>
                    Vision
                    A just, safe and equitable society that upholds the rights of tillers to own, control, secure,
                    cultivate and enhance their agricultural lands, improve their quality of life towards rural
                    development and national industrialization
                </p>
